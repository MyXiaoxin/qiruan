package entity;

public class RepairPerson {
	private int id;
	private String password;
	private String tx_url;
	private String uesr_sex;
	private String user_dept;
	private String name;
	private String phone;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getTx_url() {
		return tx_url;
	}
	public void setTx_url(String tx_url) {
		this.tx_url = tx_url;
	}
	public String getUesr_sex() {
		return uesr_sex;
	}
	public void setUesr_sex(String uesr_sex) {
		this.uesr_sex = uesr_sex;
	}
	public String getUser_dept() {
		return user_dept;
	}
	public void setUser_dept(String user_dept) {
		this.user_dept = user_dept;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}

	

}
